public class procedimiento {
    private int id_procedimiento;
    private String nombre;
    private String fecregis;
    private String estado;

    public int getId_procedimiento() {
        return id_procedimiento;
    }

    public void setId_procedimiento(int id_procedimiento) {
        this.id_procedimiento = id_procedimiento;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getFecregis() {
        return fecregis;
    }

    public void setFecregis(String fecregis) {
        this.fecregis = fecregis;
    }

    public String getEstado() {
        return estado;
    }

    public void setEstado(String estado) {
        this.estado = estado;
    }
    
    
}
